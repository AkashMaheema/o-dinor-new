            <section>
                <h2>Elevating your appearance instantly</h2>
                <p>A well-fitted jacket can transform your look from casual to polished in seconds. It adds structure to your
                    silhouette, creating a more refined and put-together appearance. Whether you're wearing a simple shirt
                    and trousers or a dress, adding a jacket instantly elevates your outfit to a more professional level.</p>
            </section>

            <!-- Versatility for various office settings -->
            <section>
                <h2>Versatility for various office settings</h2>
                <p>Jackets offer unparalleled versatility in the workplace. They can be dressed up or down to suit different
                    office environments and occasions:<br><br>

                    Formal meetings: Pair with matching trousers for a full suit look<br><br>

                    Casual Fridays: Wear over a t-shirt with jeans for a smart-casual vibe<br><br>

                    Client presentations: Layer over a dress shirt for a sharp, authoritative appearance<br><br>

                    After-work events: Remove for a more relaxed look<br><br></p>
